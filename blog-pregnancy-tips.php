    <p class="lead">Pregnancy is one of the most beautiful journeys in life, but it also comes with its share of questions and concerns. As gynecologists at Sankalp Hospital, we often encounter anxious expectant mothers who want to know how to ensure a healthy pregnancy. Here are our expert recommendations.</p>

    <h2>Nutrition During Pregnancy</h2>
    <p>Good nutrition is the foundation of a healthy pregnancy. What you eat directly affects your baby's growth and development. Here are the key nutrients you need:</p>
    <ul>
      <li><strong>Folic Acid:</strong> Start taking 400-800 mcg daily even before conception. It prevents neural tube defects in the baby.</li>
      <li><strong>Iron:</strong> Pregnancy increases iron requirements significantly. Include leafy greens, red meat, and iron supplements.</li>
      <li><strong>Calcium:</strong> Essential for baby's bone development. Dairy products, fortified foods, and supplements help meet needs.</li>
      <li><strong>Protein:</strong> Build baby's tissues with adequate protein from eggs, fish, pulses, and lean meats.</li>
      <li><strong>Omega-3:</strong> Important for brain development. Include fish, walnuts, and flaxseeds in your diet.</li>
    </ul>

    <blockquote>"Eating for two doesn't mean eating twice as much. It means eating twice as well."</blockquote>

    <h2>Exercise and Physical Activity</h2>
    <p>Contrary to old beliefs, moderate exercise during pregnancy is safe and beneficial. It helps:</p>
    <ol>
      <li>Manage weight gain and prevent excessive weight retention after delivery</li>
      <li>Reduce pregnancy-related discomfort like back pain and swelling</li>
      <li>Improve mood and reduce stress and anxiety</li>
      <li>Prepare your body for labor and speed up recovery</li>
      <li>Lower risk of gestational diabetes and hypertension</li>
    </ol>
    <p>Walking, swimming, prenatal yoga, and gentle stretching are excellent choices. Aim for at least 30 minutes of moderate activity most days of the week.</p>

    <h2>Regular Prenatal Checkups</h2>
    <p>Your prenatal visits are crucial for monitoring both your health and your baby's development. At Sankalp Hospital, we recommend:</p>
    <ul>
      <li>Monthly visits during the first two trimesters</li>
      <li>Bi-weekly visits in the third trimester until 36 weeks</li>
      <li>Weekly visits from 36 weeks until delivery</li>
    </ul>
    <p>During these visits, we monitor blood pressure, weight gain, fetal heartbeat, and perform necessary ultrasound scans to ensure everything is progressing normally.</p>

    <h2>Things to Avoid</h2>
    <p>Just as important as knowing what to do is knowing what to avoid:</p>
    <ul>
      <li><strong>Smoking and Alcohol:</strong> Both can cause serious birth defects and developmental issues.</li>
      <li><strong>Raw or Undercooked Food:</strong> Risk of foodborne illnesses that can harm your baby.</li>
      <li><strong>Certain Medications:</strong> Always consult your doctor before taking any medication.</li>
      <li><strong>High-Impact Exercises:</strong> Avoid activities with risk of falls or abdominal trauma.</li>
      <li><strong>Stress and Exhaustion:</strong> Listen to your body and rest when needed.</li>
    </ul>

    <h2>Warning Signs to Watch For</h2>
    <p>While most pregnancies proceed normally, be alert for these warning signs that require immediate medical attention:</p>
    <ul>
      <li>Severe headache or vision changes</li>
      <li>Heavy vaginal bleeding</li>
      <li>Severe abdominal pain</li>
      <li>Reduced fetal movements after 28 weeks</li>
      <li>Sudden swelling of face, hands, or feet</li>
      <li>Fever above 100.4°F (38°C)</li>
    </ul>
    <p>If you experience any of these symptoms, don't wait - contact Sankalp Hospital immediately or visit our 24/7 emergency services.</p>

    <h2>Preparing for Delivery</h2>
    <p>As your due date approaches, it's wise to be prepared. Consider these preparations:</p>
    <ul>
      <li>Pack your hospital bag with essentials for you and baby</li>
      <li>Know the signs of labor - regular contractions, water breaking, bloody show</li>
      <li>Plan your route to the hospital and know who will take you</li>
      <li>Discuss pain relief options with your doctor</li>
      <li>Learn breathing techniques for labor</li>
    </ul>

