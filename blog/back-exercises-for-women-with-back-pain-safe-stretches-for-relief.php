    <p>Seek medical attention if your back pain is accompanied by:</p>
    <ul>
      <li>Numbness or tingling in the legs</li>
      <li>Severe pain that prevents daily activities</li>
      <li>Pain following a fall or injury</li>
      <li>Fever or unexplained weight loss</li>
    </ul>

