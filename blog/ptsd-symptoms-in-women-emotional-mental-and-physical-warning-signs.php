    <p class="lead">Post-Traumatic Stress Disorder (PTSD) is a mental health condition triggered by experiencing or witnessing a terrifying event. While it can affect anyone, women are twice as likely as men to develop PTSD and often experience more intense symptoms.</p>

    <h2>Emotional and Mental Symptoms</h2>
    <p>PTSD affects the way you think and feel about yourself and others. Common emotional signs include:</p>
    <ul>
      <li><strong>Intrusive Memories:</strong> Recurrent, unwanted distressing memories of the traumatic event or reliving the event as if it were happening again (flashbacks).</li>
      <li><strong>Avoidance:</strong> Trying to avoid thinking or talking about the event, or avoiding places and people that remind you of it.</li>
      <li><strong>Negative Changes in Thinking:</strong> Negative thoughts about yourself, other people, or the world; hopelessness about the future; or feeling detached from family and friends.</li>
      <li><strong>Emotional Numbness:</strong> Difficulty experiencing positive emotions or a lack of interest in activities you once enjoyed.</li>
    </ul>

    <div class="mental-box">
      <h4>Physical Warning Signs</h4>
      <p>PTSD isn't just in the mind; it manifests physically as well:</p>
      <ul>
        <li><strong>Hypervigilance:</strong> Always being on guard or easily startled.</li>
        <li><strong>Sleep Disturbances:</strong> Difficulty falling asleep or staying asleep, or experiencing vivid nightmares.</li>
        <li><strong>Physical Arousal:</strong> Sudden outbursts of anger, irritability, or aggressive behavior.</li>
        <li><strong>Concentration Issues:</strong> Difficulty focusing on tasks or remembering things.</li>
      </ul>
    </div>

    <h2>Why PTSD is Different for Women</h2>
    <p>Women are more likely to experience trauma related to interpersonal violence, which can lead to higher rates of "Complex PTSD." Women also tend to experience more symptoms of anxiety and depression alongside PTSD.</p>

    <h2>Seeking Help and Treatment</h2>
    <p>Recovery is possible. Effective treatments include trauma-focused psychotherapy (such as CBT or EMDR) and sometimes medication. At Sankalp Hospital, we believe in a holistic approach to health that includes mental well-being.</p>

