    <p class="lead">Early detection is the single most important factor in surviving cancer. For women, being in tune with your body and recognizing unusual changes can lead to early diagnosis and more effective treatment outcomes.</p>

    <h2>Symptoms You Should Not Ignore</h2>
    <p>While many of these symptoms can be caused by less serious conditions, any persistent change should be evaluated by a medical professional.</p>

    <h3>1. Breast Changes</h3>
    <p>Beyond finding a lump, look for changes in breast size or shape, skin dimpling, nipple discharge (other than milk), or persistent pain. Regular self-exams and annual mammograms (after 40) are essential.</p>

    <h3>2. Abnormal Vaginal Bleeding</h3>
    <p>Bleeding between periods, after intercourse, or after menopause should always be investigated. This can be a sign of cervical, uterine, or ovarian cancer.</p>

    <h3>3. Persistent Bloating and Pelvic Pain</h3>
    <p>Feeling bloated almost every day for several weeks, especially when accompanied by pelvic pain or difficulty eating, can be a subtle sign of ovarian cancer.</p>

    <h3>4. Skin Changes</h3>
    <p>A mole that changes shape, color, or size, or a sore that doesn't heal, can be a sign of skin cancer. Check your body regularly, including hard-to-see areas.</p>

    <div class="warning-box">
      <h4>The Importance of Screenings</h4>
      <p>Screenings like Pap smears, mammograms, and colonoscopies can find cancer before symptoms even appear. At Sankalp Hospital, we offer comprehensive preventive health packages tailored for women.</p>
    </div>

    <h3>5. Unexplained Weight Loss and Fatigue</h3>
    <p>Losing 5 kg or more without trying can be an early sign of various cancers. Similarly, fatigue that doesn't improve with rest warrants a medical evaluation.</p>

    <h2>Taking Control of Your Health</h2>
    <p>Knowing your family history and maintaining a healthy lifestyle—including a balanced diet and avoiding tobacco—can significantly reduce your risk. Early intervention saves lives.</p>

