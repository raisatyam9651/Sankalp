<h3>6. थायरॉयड विकार (Hypothyroidism)</h3>
<p>जब थायरॉयड ग्रंथि अंडरएक्टिव (कम सक्रिय) होती है, तो शरीर का मेटाबॉलिज्म धीमा हो जाता है। क्रोनिक हाइपोथायरायडिज्म के मामलों में, तरल पदार्थ का जमाव होने से नसों पर दबाव पड़ता है, जिससे परिधीय न्यूरोपैथी (Peripheral neuropathy) विकसित होती है।</p>

    <h3>1. नसों पर दबाव (Nerve Compression)</h3>
    <img src="https://images.unsplash.com/photo-1579684385127-1ef15d508118?w=800&h=400&fit=crop" alt="नर्व प्रेशर" class="blog-img-large" style="height:250px;margin:20px 0">
    <p>जब किसी नस पर अधिक देर तक दबाव पड़ता है, तो रक्त का प्रवाह कम हो जाता है और झनझनाहट होने लगती है। यह आमतौर पर इन स्थितियों में होती है:</p>
    <ul>
      <li>पैर को लंबे समय तक दबाकर बैठने से</li>
      <li>सीने में करकर सोने से (क्रॉस-लेग स्लीपिंग)</li>
      <li>बहुत कसकर जूते पहनने से</li>
      <li>लंबे समय तक साइकिल चलाने से</li>
      <li>टॉयलेट पर बैठे रहने से</li>
    </ul>
<h2>अन्य महत्वपूर्ण कारण</h2>
<ul>
<li><strong>अत्यधिक शराब का सेवन:</strong> लंबे समय तक भारी मात्रा में शराब पीने से नसों में विषाक्तता (Toxicity) फैलती है जिसे अल्कोहलिक न्यूरोपैथी कहते हैं।</li>
<li><strong>दवाइयों के साइड इफेक्ट्स:</strong> कैंसर की कीमोथेरेपी (Chemotherapy), कुछ एंटीबायोटिक्स और हाई ब्लड प्रेशर की दवाइयां नसों पर असर डाल सकती हैं।</li>
<li><strong>किडनी की विफलता (Kidney Failure):</strong> रक्त में यूरिया और अन्य टॉक्सिन्स के जमा होने (यूरेमिया) से भी नसों को नुकसान पहुंचता है।</li>
</ul>

    <h3>2. विटामिन बी12 की कमी</h3>
    <p>विटामिन बी12 तंत्रिका तंत्र के स्वस्थ रहने के लिए अत्यंत जरूरी है। यह मिएलिन शीथ (myelin sheath) के निर्माण में मदद करता है जो तंत्रिकाओं की रक्षा करता है। इसकी कमी से:</p>
    <ul>
      <li>पैरों में सुन्नपन और झनझनाहट</li>
      <li>मांसपेशियों में कमजोरी</li>
      <li>संतुलन में कठिनाई</li>
      <li>याददाश्त की समस्या</li>
      <li>थकान और कमजोरी</li>
    </ul>
<h2>लक्षण जिन्हें नजरअंदाज नहीं करना चाहिए (Red Flag Symptoms)</h2>
<p>पैरों में झनझनाहट के साथ यदि आपको इनमें से कोई भी लक्षण दिखाई दे, तो तुरंत न्यूरोलॉजिस्ट (Neurologist) से संपर्क करें:</p>
