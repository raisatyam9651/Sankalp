    <h3>1. नसों पर दबाव (Nerve Compression)</h3>
    <p>जब किसी नस पर अधिक देर तक दबाव पड़ता है, तो रक्त का प्रवाह कम हो जाता है और झनझनाहट होने लगती है। यह आमतौर पर:</p>
    <ul>
