<h3>3. विटामिन B12 की कमी (Vitamin B12 Deficiency)</h3>
<p>विटामिन B12 तंत्रिकाओं (Nerves) के बाहरी सुरक्षा कवच (Myelin sheath) को बनाने और स्वस्थ रखने के लिए अत्यंत आवश्यक है। इसकी कमी होने पर यह कवच टूटने लगता है, जिससे तंत्रिकाएं सही से काम नहीं कर पातीं। इसके परिणामस्वरूप पैरों और हाथों में झनझनाहट, सुई चुभने जैसा अहसास, अत्यधिक थकान और याददाश्त कमजोर होने जैसी समस्याएं होती हैं। यह शाकाहारी लोगों में बहुत आम है।</p>

    <h2>पैर में झनझनाहट क्या है?</h2>
    <p>झनझनाहट एक संवेदी लक्षण है जब तंत्रिकाओं पर दबाव पड़ता है या वे क्षतिग्रस्त हो जाती हैं। इससे संकेतों का प्रवाह बाधित होता है और मस्तिष्क को असामान्य संकेत मिलते हैं, जिससे झनझनाहट, सुन्नपन, या जलन जैसा अनुभव होता है।</p>
<h3>4. परिधीय धमनी रोग (Peripheral Artery Disease - PAD)</h3>
<p>यह रक्त संचार से जुड़ी एक बीमारी है जिसमें पैरों तक शुद्ध रक्त ले जाने वाली धमनियों (Arteries) में फैट (प्लाक) जमा हो जाता है और वे संकरी हो जाती हैं। पैरों तक पर्याप्त ऑक्सीजन और रक्त न पहुंचने के कारण उनमें झनझनाहट, ठंडक और चलते समय मांसपेशियों में ऐंठन (Cramps) होती है।</p>

    <p>झनझनाहट दो प्रकार की होती है:</p>
    <ul>
      <li><strong>अस्थायी झनझनाहट:</strong> यह आमतौर पर किसी नस पर अधिक देर दबाव पड़ने से होती है और जल्दी ठीक हो जाती है</li>
      <li><strong>स्थायी झनझनाहट:</strong> यह तंत्रिका क्षति या किसी चिकित्सा स्थिति का संकेत हो सकती है</li>
    </ul>
<h3>5. ऑटोइम्यून बीमारियां (Autoimmune Diseases)</h3>
<p>रूमेटाइड अर्थराइटिस (RA), ल्यूपस या मल्टीपल स्केलेरोसिस (MS) जैसी बीमारियों में शरीर का इम्यून सिस्टम गलती से अपनी ही स्वस्थ नसों और ऊतकों पर हमला करने लगता है। इसके कारण नसों में सूजन आ जाती है और पैरों में सुन्नपन व झनझनाहट शुरू हो जाती है।</p>

