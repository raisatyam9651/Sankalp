    <h3>4. परिधीय न्यूरोपैथी (Peripheral Neuropathy)</h3>
    <p>यह एक ऐसी स्थिति है जहां शरीर की परिधीय नसें (वे नसें जो मस्तिष्क और रीढ़ की हड्डी से दूर हैं) क्षतिग्रस्त हो जाती हैं। इससे:</p>
    <ul>
