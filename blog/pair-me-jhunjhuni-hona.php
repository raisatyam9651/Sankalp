<ul class="mb-0 text-dark">
<li>झनझनाहट का अचानक शुरू होना और तेजी से पूरी टांगों में फैल जाना।</li>
<li>पैरों में इतनी कमजोरी आना कि चलने में लड़खड़ाहट हो या आप गिर जाएं (लखवा/Paralysis का संकेत)।</li>
<li>मूत्र या मल विसर्जन पर नियंत्रण न रहना (Bowel/Bladder Incontinence)।</li>
<li>झनझनाहट के साथ-साथ बोलने में लड़खड़ाहट या चेहरे का सुन्न होना (यह स्ट्रोक/Stroke का संकेत है, तुरंत इमरजेंसी में जाएं)।</li>
<li>पैरों में किसी घाव या कट का होना, जिसका आपको सुन्नपन के कारण दर्द महसूस न हो।</li>
</ul>
