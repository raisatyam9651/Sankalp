<h2>फेफड़ों में दर्द के मुख्य कारण (Causes of Lung Pain)</h2>
<p>फेफड़ों या उसके आस-पास महसूस होने वाले दर्द के कई मेडिकल कारण हो सकते हैं। कुछ कारण बहुत सामान्य होते हैं (जैसे मांसपेशियों का खिंचाव), जबकि कुछ मेडिकल इमरजेंसी की श्रेणी में आते हैं।</p>

<h3>1. प्लूरिसी (Pleurisy)</h3>
<p>हमारे फेफड़ों और छाती की गुहा (Chest cavity) के बीच एक दो-परत वाली पतली झिल्ली होती है जिसे 'प्लूरा' (Pleura) कहते हैं। जब वायरल या बैक्टीरियल इन्फेक्शन के कारण इस झिल्ली में सूजन आ जाती है, तो इसे प्लूरिसी कहते हैं। जब आप सांस अंदर लेते हैं या खांसते हैं, तो सूजी हुई परतें आपस में रगड़ खाती हैं, जिससे सीने में चुभने वाला बहुत तेज दर्द होता है।</p>

<h3>2. निमोनिया (Pneumonia)</h3>
<p>निमोनिया फेफड़ों का एक गंभीर संक्रमण है, जो बैक्टीरिया, वायरस या फंगस के कारण होता है। इसमें फेफड़ों की वायु-थैलियों (Alveoli) में मवाद या तरल पदार्थ भर जाता है। निमोनिया के कारण सीने में गहरा दर्द, तेज बुखार, ठंड लगना, और पीला या हरा बलगम वाली खांसी आती है।</p>

<h3>3. पल्मोनरी एम्बोलिज्म (Pulmonary Embolism)</h3>
<p>यह एक मेडिकल इमरजेंसी है। इसमें पैरों की नसों में बना खून का थक्का (Blood clot) टूटकर फेफड़ों की किसी धमनी में फंस जाता है, जिससे रक्त का प्रवाह रुक जाता है। इसमें अचानक सीने में तेज दर्द (जो सांस लेने पर बढ़ता है), सांस फूलना, और कभी-कभी खांसी में खून आने की समस्या होती है।</p>

<h3>4. अस्थमा (Asthma) और COPD</h3>
<p>अस्थमा और क्रॉनिक ऑब्सट्रक्टिव पल्मोनरी डिजीज (COPD) में श्वास नलियों में पुरानी सूजन आ जाती है और वे सिकुड़ जाती हैं। हवा के गुजरने में रुकावट के कारण छाती में भारीपन, जकड़न और दर्द महसूस होता है। अक्सर यह घरघराहट (Wheezing) के साथ होता है।</p>

<h3>5. न्यूमोथोरैक्स (Pneumothorax - फेफड़े का सिकुड़ना)</h3>
<p>यह तब होता है जब फेफड़े और छाती की दीवार के बीच हवा भर जाती है, जिससे फेफड़ा पिचक (Collapse) जाता है। इसके कारण सीने में एक तरफ अचानक तेज दर्द होता है और मरीज को लगता है कि उसे हवा नहीं मिल पा रही है।</p>

<h3>6. कोस्टोकॉन्ड्राइटिस (Costochondritis)</h3>
<p>पसलियों को ब्रेस्टबोन (Sternum) से जोड़ने वाले कार्टिलेज (उपास्थि) में सूजन आने को कोस्टोकॉन्ड्राइटिस कहते हैं। यह दर्द बिल्कुल हार्ट अटैक जैसा महसूस होता है और गहरी सांस लेने या उस जगह को दबाने पर बहुत तेज हो जाता है।</p>

