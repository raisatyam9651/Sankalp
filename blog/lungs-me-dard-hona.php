<h2>जांच और निदान (Diagnosis)</h2>
<p>चूंकि सीने का दर्द जानलेवा भी हो सकता है, इसलिए डॉक्टर सटीक कारण जानने के लिए कुछ महत्वपूर्ण टेस्ट करवाते हैं:</p>
<ul>
<li><strong>चेस्ट एक्स-रे (Chest X-Ray):</strong> निमोनिया, न्यूमोथोरैक्स या फेफड़ों में पानी (Pleural effusion) का पता लगाने के लिए।</li>
<li><strong>सीटी स्कैन (CT Scan):</strong> फेफड़ों की 3D इमेज के जरिए पल्मोनरी एम्बोलिज्म या ट्यूमर की जांच के लिए।</li>
<li><strong>ECG (इलेक्ट्रोकार्डियोग्राम):</strong> यह सुनिश्चित करने के लिए कि दर्द दिल की किसी बीमारी (हार्ट अटैक) के कारण तो नहीं है।</li>
<li><strong>पल्मोनरी फंक्शन टेस्ट (PFT):</strong> अस्थमा या COPD का पता लगाने के लिए कि फेफड़े कितनी अच्छी तरह काम कर रहे हैं।</li>
<li><strong>पल्स ऑक्सीमेट्री:</strong> रक्त में ऑक्सीजन के स्तर की जांच।</li>
</ul>

<h2>बचाव और फेफड़ों को स्वस्थ रखने के उपाय</h2>
<p>फेफड़ों की क्षमता बढ़ाने और उन्हें संक्रमण से बचाने के लिए अपनी जीवनशैली में कुछ महत्वपूर्ण बदलाव करना आवश्यक है:</p>
<ul>
<li><strong>धूम्रपान छोड़ें (Quit Smoking):</strong> सिगरेट का धुआं फेफड़ों के कैंसर, COPD और अस्थमा का सबसे बड़ा कारण है। धूम्रपान तुरंत छोड़ें और सेकंड-हैंड स्मोक से भी बचें।</li>
<li><strong>डीप ब्रीदिंग एक्सरसाइज:</strong> नियमित रूप से प्राणायाम (जैसे अनुलोम-विलोम), योगा और ब्रीदिंग एक्सरसाइज करें। यह फेफड़ों की वायु क्षमता (Lung capacity) को बढ़ाता है।</li>
<li><strong>टीकाकरण (Vaccination):</strong> निमोनिया (Pneumococcal) और फ्लू (Flu) की वार्षिक वैक्सीन जरूर लगवाएं, विशेषकर यदि आपकी उम्र 50 वर्ष से अधिक है या आपको अस्थमा है।</li>
<li><strong>प्रदूषण से बचें:</strong> अत्यधिक धूल, धुएं या केमिकल वाले माहौल में काम करते समय हमेशा N95 मास्क पहनें।</li>
<li><strong>हाइड्रेशन:</strong> दिनभर में पर्याप्त पानी पिएं। पानी फेफड़ों और श्वास नली के बलगम (Mucus) को पतला रखता है, जिससे इसे बाहर निकालना आसान होता है।</li>
</ul>

