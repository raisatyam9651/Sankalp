    <h2>फेफड़ों में दर्द के कारण</h2>
    <p>फेफड़ों में दर्द कई कारणों से हो सकता है। कुछ सामान्य और कुछ गंभीर:</p>

    <h3>1. प्लूरिसी (Pleurisy)</h3>
    <p>फेफड़ों की सतह (प्लूरा) पर सूजन से सांस लेने पर तेज दर्द होता है। यह दर्द सांस लेने पर बढ़ता है और छाती के एक तरफ अधिक होता है। प्लूरिसी विभिन्न संक्रमणों या सूजन संबंधी रोगों से हो सकता है।</p>
    <p><strong>लक्षण:</strong> सांस लेने पर तेज दर्द, छाती में जलन, सांस लेने में कठिनाई, सूखी खांसी।</p>

    <h3>2. निमोनिया (Pneumonia)</h3>
    <p>फेफड़ों में बैक्टीरियल या वायरल इंफेक्शन से निमोनिया होती है। इसमें फेफड़ों में सूजन और तरल पदार्थ जमा हो जाता है। निमोनिया बच्चों और बुजुर्गों में अधिक खतरनाक होती है।</p>
    <p><strong>लक्षण:</strong> खांसी (कभी-कभी बलगम के साथ), बुखार और ठंड लगना, सांस लेने में कठिनाई, थकान और कमजोरी।</p>

    <h3>3. पंक्टर न्यूमोथोरैक्स (Pneumothorax)</h3>
    <p>फेफड़े में हवा का रिसाव जिससे फेफड़ा ढह सकता है। यह अचानक हो सकता है और तेज दर्द होता है। कुछ मामलों में यह चोट से होता है, जबकि कुछ मामलों में बिना किसी कारण के।</p>
    <p><strong>लक्षण:</strong> अचानक तेज दर्द, सांस लेने में कठिनाई, सांस की तेजी से धड़कन।</p>

    <h3>4. अस्थमा (Asthma)</h3>
    <p>अस्थमा में सांस की नलियां सिकुड़ जाती हैं जिससे सांस लेने में कठिनाई और दर्द होता है। यह एक पुरानी बीमारी है जो अक्सर एलर्जी या पर्यावरणीय कारकों से बढ़ती है।</p>
    <p><strong>लक्षण:</strong> घरघराहट, सांस लेने में कठिनाई, सीने में जकड़न, खांसी (विशेषकर रात को)।</p>

    <h3>5. पसलियों का दर्द और चोट</h3>
    <p>पसलियों में चोट या सूजन से छाती में दर्द होता है जो फेफड़ों में दर्द जैसा महसूस होता है। पसलियों की फ्रैक्चर या मांसपेशियों में खिंचाव भी इसका कारण हो सकता है।</p>
    <p><strong>लक्षण:</strong> छाती में दर्द जो सांस लेने पर बढ़ता है, स्पर्श करने पर दर्द, सांस गहरा लेने में कठिनाई।</p>

    <h3>6. फेफड़ों का कैंसर</h3>
    <p>फेफड़ों का कैंसर एक गंभीर बीमारी है जिसमें फेफड़ों में दर्द हो सकता है। शुरुआती चरण में अक्सर कोई लक्षण नहीं दिखते, इसलिए नियमित जांच जरूरी है।</p>
    <p><strong>लक्षण:</strong> लगातार खांसी, खांसी में खून, वजन कम होना, थकान।</p>

    <h3>7. पल्मोनरी एम्बोलिज्म (Pulmonary Embolism)</h3>
    <p>फेफड़ों की धमनी में खून का थक्का अटक जाना एक गंभीर स्थिति है। यह अचानक होती है और तुरंत चिकित्सा ध्यान की आवश्यकता होती है।</p>
    <p><strong>लक्षण:</strong> अचानक सांस लेने में कठिनाई, तेज दर्द, तेज दिल की धड़कन, बेहोशी।</p>

    <h3>8. COPD (Chronic Obstructive Pulmonary Disease)</h3>
    <p>COPD एक पुरानी बीमारी है जिसमें वायुमार्ग अवरुद्ध हो जाते हैं। यह धूम्रपान से अधिक होती है। इसमें फेफड़ों में दर्द और सांस लेने में कठिनाई होती है।</p>

