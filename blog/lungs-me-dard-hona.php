    <h3>1. आराम करें</h3>
    <p>फेफड़ों में दर्द होने पर पर्याप्त आराम जरूरी है। भारी व्यायाम और शारीरिक मेहनत से बचें। सोने के लिए सिर को ऊपर उठाए रखें।</p>

    <h3>2. गर्म सेक</h3>
    <p>छाती पर गर्म कपड़ा रखें। यह दर्द और मांसपेशियों के तनाव से राहत देता है। बजाया भाप लेना भी फायदेमंद हो सकता है।</p>

    <h3>3. सांस व्यायाम</h3>
    <p>गहरी सांस लेने का अभ्यास करें। नाक से धीरे-धीरे सांस अंदर लें, 4 गिनती तक रोकें, और मुंह से धीरे-धीरे छोड़ें। यह फेफड़ों की क्षमता बढ़ाता है।</p>

    <h3>4. तरल पदार्थ</h3>
    <p>पर्याप्त पानी पिएं। यह बलगम को पतला करता है और सांस लेना आसान बनाता है। गर्म तरल पदार्थ जैसे सूप या चाय और भी फायदेमंद हैं।</p>

    <h2>रोकथाम के उपाय</h2>
    <p>फेफड़ों के स्वास्थ्य को बनाए रखने और बीमारियों से बचने के लिए:</p>
    <ul>
      <li><strong>धूम्रपान न करें:</strong> धूम्रपान फेफड़ों को सबसे ज्यादा नुकसान पहुंचाता है। यदि आप धूम्रपान करते हैं, तो छोड़ना सबसे अच्छा कदम है।</li>
      <li><strong>वायु प्रदूषण से बचें:</strong> धूल, धुंध, और रसायनों से दूर रहें। जब प्रदूषण अधिक हो तो मास्क पहनें।</li>
