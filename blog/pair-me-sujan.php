<li>अचानक केवल एक पैर में तेज सूजन और दर्द होना (DVT का संकेत)।</li>
<li>सूजन के साथ छाती में दर्द, सांस लेने में तकलीफ या चक्कर आना (हृदय की समस्या का संकेत)।</li>
<li>प्रभावित पैर की त्वचा का लाल होना, छूने पर गर्म लगना या बुखार आना (सेल्युलाइटिस या संक्रमण)।</li>
<li>यदि आप गर्भवती हैं और अचानक आपके चेहरे, हाथों या पैरों में तेज सूजन आ गई है।</li>
<li>यदि घरेलू उपायों के बावजूद सूजन कई दिनों तक बनी रहे या बढ़ती जाए।</li>
</ul>
