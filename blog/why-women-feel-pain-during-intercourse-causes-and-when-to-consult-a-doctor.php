    <p class="lead">Painful intercourse, medically known as dyspareunia, is a common issue that many women experience at some point in their lives. While it can be a difficult topic to discuss, understanding the causes is the first step toward finding relief and restoring your sexual health.</p>

    <h2>Common Physical Causes</h2>
    <p>Pain can occur at the entry or deep within the pelvis, and the location often helps identify the cause:</p>

    <h3>1. Inadequate Lubrication</h3>
    <p>This is often the result of not enough foreplay, but it can also be caused by hormonal changes (such as menopause or breastfeeding), certain medications, or stress.</p>

    <h3>2. Infections and Skin Conditions</h3>
    <p>Infections like yeast infections, UTIs, or STIs can cause significant discomfort and inflammation. Skin conditions like eczema or lichen sclerosus in the genital area can also be a factor.</p>

    <h3>3. Gynecological Conditions</h3>
    <p>Deep pelvic pain during intercourse is often linked to underlying conditions such as:</p>
    <ul>
      <li><strong>Endometriosis:</strong> When uterine tissue grows outside the uterus.</li>
      <li><strong>Pelvic Inflammatory Disease (PID):</strong> Infection of the reproductive organs.</li>
      <li><strong>Uterine Fibroids:</strong> Non-cancerous growths in the uterus.</li>
      <li><strong>Ovarian Cysts:</strong> Large cysts can cause pain during deep penetration.</li>
    </ul>

    <div class="care-box">
      <h4>Emotional and Psychological Factors</h4>
      <p>The mind and body are closely linked. Anxiety, depression, concerns about body image, or a history of trauma can all contribute to physical pain during intimacy.</p>
    </div>

    <h2>When to Consult a Gynecologist</h2>
    <p>You should not ignore persistent pain during intercourse. Consult a specialist at Sankalp Hospital if:</p>
    <ul>
      <li>The pain is severe or has started recently.</li>
      <li>It is accompanied by unusual discharge, bleeding, or itching.</li>
      <li>The pain is affecting your relationship or emotional well-being.</li>
    </ul>

