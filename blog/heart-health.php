<li><strong>Limit Saturated & Trans Fats:</strong> Reduce red meat, butter, and processed foods</li>
<li><strong>Reduce Sodium:</strong> Keep salt intake under 2,300mg daily</li>
<li><strong>Watch Portion Sizes:</strong> Use smaller plates and avoid overeating</li>
</ul>

<h2>2. Stay Physically Active</h2>
<p>Regular exercise strengthens your heart and improves circulation:</p>
<ul>
<li><strong>Aerobic Exercise:</strong> 150 minutes of moderate activity (walking, cycling) weekly</li>
<li><strong>Strength Training:</strong> 2-3 sessions per week for muscle health</li>
<li><strong>Stay Moving:</strong> Take breaks from sitting every hour</li>
<li><strong>Start Slowly:</strong> If new to exercise, begin with 10-minute walks and gradually increase</li>
</ul>

<h2>3. Maintain a Healthy Weight</h2>
<p>Being overweight increases your risk of heart disease, diabetes, and high blood pressure:</p>
<ul>
<li>Calculate your BMI (Body Mass Index)</li>
<li>Target a waist circumference under 40 inches (men) or 35 inches (women)</li>
<li>Lose weight gradually through diet and exercise</li>
<li>Even a 5-10% reduction in body weight improves heart health</li>
</ul>

