<h2>रक्तचाप (Blood Pressure) को समझना</h2>
<p>हमारा हृदय शरीर के सभी अंगों तक ऑक्सीजन और पोषक तत्व पहुंचाने के लिए धमनियों (Arteries) के माध्यम से रक्त पंप करता है। इस प्रक्रिया में रक्त धमनियों की दीवारों पर जो दबाव डालता है, उसे ब्लड प्रेशर (BP) कहते हैं। इसे दो अंकों में मापा जाता है (उदा. 120/80 mmHg):</p>
<ul>
<li><strong>सिस्टोलिक (Systolic BP) - ऊपर का अंक:</strong> यह तब का दबाव है जब हृदय सिकुड़ता है और शरीर में रक्त धकेलता है।</li>
<li><strong>डायस्टोलिक (Diastolic BP) - नीचे का अंक:</strong> यह तब का दबाव है जब हृदय दो धड़कनों के बीच आराम करता है और रक्त से भरता है।</li>
</ul>

