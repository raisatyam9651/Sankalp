    <div class="thyroid-box" style="border-left-color:#ef5350; background:#ffebee">
      <h4 style="color:#c62828">2. Hyperthyroidism (Overactive Thyroid)</h4>
      <p>When the thyroid produces too much hormone, your body's systems speed up. Symptoms include rapid heartbeat, anxiety, weight loss despite increased appetite, and sensitivity to heat.</p>
    </div>

