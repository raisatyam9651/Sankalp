<h2>The Connection Between Heart Attack and Cardiac Arrest</h2>
<p>While they are different conditions, a heart attack can <strong>trigger</strong> cardiac arrest. When a large area of heart muscle dies during a heart attack, it can cause electrical instability that leads to <a href="/emergency-hospital-in-ambikapur"><strong>ventricular fibrillation</strong></a> - the chaotic heart rhythm that causes cardiac arrest.</p>

<p>Other heart conditions that can lead to cardiac arrest include:</p>
<ul>
  <li><a href="/blog/heart-health"><strong>Heart failure</strong></a> (weakened heart muscle)</li>
  <li><strong>Arrhythmias</strong> (irregular heartbeats)</li>
  <li><strong>Electrocution</strong> or severe trauma</li>
  <li><strong>Severe electrolyte imbalances</strong></li>
  <li><strong>Certain medications</strong> that affect heart rhythm</li>
</ul>

<h2>What to Do in an Emergency</h2>

<h3>If You Suspect a Heart Attack:</h3>
<ol>
  <li><strong>Call Emergency Services Immediately</strong> - Don't wait or try to drive yourself</li>
  <li><strong> Chew an Aspirin</strong> (if not allergic) - This can help reduce clotting</li>
  <li><strong>Stay Calm and Rest</strong> - Physical activity increases heart oxygen demand</li>
  <li><strong>Loosen Tight Clothing</strong> - Make breathing easier</li>
  <li><strong>If Prescribed, Take Nitroglycerin</strong> - Follow your doctor's instructions</li>
  <li><strong>Be Ready for CPR</strong> - Be prepared in case the person worsens</li>
</ol>

<h3>If You Suspect Cardiac Arrest:</h3>
<ol>
  <li><strong>Call Emergency Services Immediately</strong> - Every second counts</li>
  <li><strong>Start CPR Immediately</strong> - Push hard and fast in the center of chest (100-120 compressions/minute)</li>
  <li><strong>Use an AED (Automated External Defibrillator)</strong> if available - Follow the voice prompts</li>
  <li><strong>Don't Stop CPR</strong> - Continue until help arrives</li>
  <li><strong>Place Person in Recovery Position</strong> - Only if they start breathing normally</li>
</ol>

<div class="alert alert-success mt-3 mb-4 shadow-sm border-0" style="background-color: #d4edda; border-left: 5px solid #28a745 !important;">
  <strong>Hands-Only CPR:</strong> If you're not trained in CPR, just push hard and fast in the center of the chest (at least 5cm deep, 100-120 times per minute) until help arrives. You don't need to give rescue breaths.
</div>

<h2>Prevention: Protecting Your Heart</h2>
<p>Both heart attack and cardiac arrest can often be prevented by maintaining a <a href="/blog/heart-health"><strong>healthy heart</strong></a> through lifestyle choices:</p>

<h3>Lifestyle Changes for Heart Health:</h3>
<ul>
  <li><strong>Regular Exercise:</strong> At least 30 minutes, 5 days a week</li>
  <li><strong>Heart-Healthy Diet:</strong> Fruits, vegetables, whole grains, lean proteins</li>
  <li><strong>Maintain Healthy Weight:</strong> Obesity increases heart disease risk</li>
  <li><strong>Quit Smoking:</strong> Smoking damages arteries and increases clot formation</li>
  <li><strong>Limit Alcohol:</strong> Excessive drinking raises blood pressure</li>
  <li><strong>Manage Stress:</strong> Chronic stress can harm your heart</li>
  <li><strong>Regular Check-ups:</strong> Monitor blood pressure, cholesterol, and diabetes</li>
</ul>

<h3>Know Your Risk Factors:</h3>
<ul>
  <li><a href="/blog/bp-high-hone-par-kya-kare"><strong>High Blood Pressure</strong></a> (hypertension)</li>
  <li>High cholesterol levels</li>
  <li>Diabetes</li>
  <li>Family history of heart disease</li>
  <li>Age (risk increases after 45 for men, 55 for women)</li>
  <li>Sedentary lifestyle</li>
</ul>

