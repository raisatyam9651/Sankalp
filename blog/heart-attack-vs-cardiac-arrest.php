<h2>What is a Heart Attack?</h2>
<p>A <a href="/blog/heart-health"><strong>heart attack</strong></a>, also known as myocardial infarction, occurs when blood flow to a part of the heart muscle is blocked, usually by a blood clot. This blockage damages or destroys part of the heart muscle due to lack of oxygen.</p>

<p>Think of it like a plumbing problem in your heart's plumbing system (coronary arteries). When one of these arteries gets blocked, the area of heart muscle it supplies begins to die.</p>

<h3>Common Causes of Heart Attack:</h3>
<ul>
  <li><strong>Coronary Artery Disease (CAD):</strong> The most common cause, where fatty deposits (plaque) build up in the arteries</li>
  <li><strong>Blood Clots:</strong> Can form at the site of plaque rupture and block blood flow</li>
  <li><strong>Spasm of the Coronary Artery:</strong> A sudden tightening that can cut off blood flow</li>
  <li><strong>Spontaneous Coronary Artery Dissection (SCAD):</strong> A rare condition where a tear forms in the artery wall</li>
</ul>

<h3>Heart Attack Symptoms May Include:</h3>
<ul>
  <li>Chest pain, pressure, or tightness (often described as an elephant sitting on chest)</li>
  <li>Pain radiating to the left arm, jaw, neck, or back</li>
  <li>Shortness of breath</li>
  <li>Cold sweat</li>
  <li>Nausea or vomiting</li>
  <li>Lightheadedness</li>
  <li>Unusual fatigue (especially in women)</li>
</ul>

<div class="alert alert-warning mt-3 mb-4 shadow-sm border-0" style="background-color: #fff3cd; border-left: 5px solid #ffc107 !important;">
  <strong>Important:</strong> Symptoms can vary between individuals. Women may experience subtler symptoms like fatigue, nausea, or back pain. Don't ignore any unusual symptoms - act fast.
</div>

<h2>What is Cardiac Arrest?</h2>
<p><a href="/emergency-hospital-in-ambikapur"><strong>Cardiac arrest</strong></a> occurs when the heart suddenly stops beating due to an electrical malfunction. The heart's pumping action stops, stopping blood flow to the brain and other vital organs. Without immediate treatment, death occurs within minutes.</p>

<p>Think of it as an electrical problem - the heart's rhythm becomes chaotic (ventricular fibrillation) or stops entirely.</p>

<h3>Common Causes of Cardiac Arrest:</h3>
<ul>
  <li><strong>Ventricular Fibrillation:</strong> The most common cause - the heart's lower chambers quiver chaotically</li>
  <li><strong>Electrolyte Imbalances:</strong> Low potassium or magnesium can trigger arrhythmias</li>
  <li><strong>Trauma or Severe Blood Loss:</strong> Shock can stop the heart</li>
  <li><strong>Drowning or Choking:</strong> Lack of oxygen can trigger arrest</li>
  <li><strong>Drug Overdose:</strong> Certain substances can disrupt heart rhythm</li>
  <li><strong>Underlying Heart Conditions:</strong> Such as <a href="/blog/heart-health"><strong>cardiomyopathy</strong></a> or congenital heart defects</li>
</ul>

<h3>Cardiac Arrest Warning Signs May Include:</h3>
<ul>
  <li>Sudden collapse or falling</li>
  <li>No pulse or heartbeat</li>
  <li>No breathing or gasping (agonal breathing)</li>
  <li>Loss of consciousness</li>
  <li>Pale or blue skin color</li>
  <li>Dilated pupils</li>
</ul>

<div class="alert alert-danger mt-3 mb-4 shadow-sm border-0" style="background-color: #f8d7da; border-left: 5px solid #dc3545 !important;">
  <strong>Cardiac Arrest is a Medical Emergency!</strong> Within 4-6 minutes without oxygen, brain damage begins. Every second counts. Call emergency services immediately.
</div>

