  <li><strong>Electrocardiogram (ECG/EKG):</strong> Records the heart's electrical activity</li>
  <li><strong>Echocardiogram:</strong> Uses sound waves to create heart images</li>
  <li><strong>Stress Test:</strong> Monitors heart function during exercise</li>
  <li><strong>Cardiac Catheterization:</strong> Examines arteries for blockages</li>
  <li><strong>CT Coronary Angiography:</strong> Detailed images of heart vessels</li>
  <li><strong>Blood Tests:</strong> Check for enzymes indicating heart damage</li>
</ul>

<h2>Treatment Options Available</h2>
<h3>For Heart Attack:</h3>
<ul>
  <li><strong>Medications:</strong> Blood thinners, clot busters, cholesterol-lowering drugs</li>
  <li><strong>Angioplasty and Stenting:</strong> Opens blocked arteries</li>
  <li><strong>Coronary Artery Bypass Surgery:</strong> Creates new routes for blood flow</li>
</ul>

<h3>For Cardiac Arrest Prevention:</h3>
<ul>
  <li><strong>Implantable Cardioverter Defibrillator (ICD):</strong> A device that shocks the heart if dangerous rhythms are detected</li>
  <li><strong>Medications:</strong> Beta-blockers, anti-arrhythmics</li>
  <li><strong>Ablation Therapy:</strong> Destroys abnormal heart tissue causing arrhythmias</li>
</ul>

