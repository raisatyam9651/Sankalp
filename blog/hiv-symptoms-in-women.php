    <div class="blog-image-text">
      <div class="text-content">
        <h3>Who Should Get Tested?</h3>
        <p>According to health guidelines, everyone between the ages of 15 and 65 should get tested at least once as part of routine healthcare. Additionally, you should get tested if you:</p>
        <ul>
          <li>Are sexually active with multiple partners</li>
          <li>Have unprotected sex</li>
          <li>Share needles or drug equipment</li>
          <li>Are pregnant or planning pregnancy</li>
          <li>Have been diagnosed with another STI</li>
          <li>Have a partner who has HIV</li>
        </ul>
