    <h2>How HIV Spreads</h2>
    <p>Understanding how HIV is transmitted is the first step in prevention. HIV spreads through specific body fluids:</p>
    <ul>
      <li><strong>Unprotected sexual contact:</strong> Vaginal, anal, or oral sex without condoms</li>
      <li><strong>Sharing needles or syringes:</strong> Drug use equipment that contains blood</li>
      <li><strong>Mother to child:</strong> During pregnancy, birth, or breastfeeding</li>
      <li><strong>Blood transfusion:</strong> Rare in countries with good blood screening</li>
    </ul>

    <div class="info-box">
      <h4>HIV Does NOT Spread Through:</h4>
      <p> Casual contact like hugging, shaking hands, or sharing food. Mosquitoes or insect bites. Sharing toilet seats or swimming pools. Saliva, tears, or sweat (unless mixed with blood). Coughing or sneezing.</p>
    </div>

