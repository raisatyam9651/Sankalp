    <h2>Early Symptoms of HIV in Women</h2>
    <p>Within 2-4 weeks after exposure, some women experience flu-like symptoms, which is the body's natural response to HIV infection. This is called Acute Retroviral Syndrome (ARS) or Primary HIV Infection.</p>

    <h3>Common Early Symptoms:</h3>
    <ul>
      <li><strong>Fever:</strong> Often high, ranging from 100-102°F (38-39°C)</li>
      <li><strong>Fatigue and weakness:</strong> Feeling extremely tired without reason</li>
      <li><strong>Sore throat:</strong> Pain or scratchiness in the throat</li>
      <li><strong>Swollen lymph nodes:</strong> Particularly in the neck, armpits, and groin</li>
      <li><strong>Headache:</strong> Persistent or severe headaches</li>
      <li><strong>Muscle aches and joint pain:</strong> Body pains similar to flu</li>
      <li><strong>Night sweats:</strong> Excessive sweating during sleep</li>
      <li><strong>Rash:</strong> Sometimes appears on the torso</li>
      <li><strong>Nausea, vomiting, and diarrhea:</strong> Gastrointestinal symptoms</li>
    </ul>

    <div class="warning-box">
      <h4>Important Note:</h4>
      <p>These early symptoms are similar to common flu and may be mistaken for other viral infections. The only way to know if you have HIV is to get tested. If you think you may have been exposed to HIV, don't wait for symptoms - get tested as soon as possible.</p>
    </div>

    <h2>Women-Specific HIV Symptoms</h2>
    <p>Women may experience certain symptoms that are unique to or more common in women with HIV:</p>

    <h3>1. Vaginal Infections and Conditions</h3>
    <p>Women with HIV are more susceptible to various vaginal infections due to their weakened immune system:</p>
    <ul>
      <li><strong>Yeast infections (Candidiasis):</strong> More frequent, severe, and recurrent yeast infections that may be difficult to treat</li>
      <li><strong>Bacterial vaginosis (BV):</strong> More common and may recur frequently</li>
      <li><strong>Recurring urinary tract infections (UTIs):</strong> More frequent and may become severe</li>
      <li><strong>Changes in vaginal discharge:</strong> Unusual color, odor, or amount</li>
    </ul>

    <h3>2. Menstrual Changes</h3>
    <p>HIV can affect the menstrual cycle in various ways:</p>
    <ul>
      <li><strong>Irregular periods:</strong> Changes in cycle length or missed periods</li>
      <li><strong>Heavy or light bleeding:</strong> Different from usual menstrual flow</li>
      <li><strong>Premenstrual syndrome (PMS) changes:</strong> More severe symptoms</li>
      <li><strong>Early menopause:</strong> Some women may experience menopause earlier</li>
    </ul>

    <h3>3. Pelvic Inflammatory Disease (PID)</h3>
    <p>Women with HIV are more likely to develop PID, and when they do, it tends to be more severe:</p>
    <ul>
      <li>More frequent episodes of PID</li>
      <li>More severe symptoms</li>
      <li>Longer recovery time</li>
      <li>Higher risk of complications</li>
    </ul>

    <h3>4. Cervical Health Issues</h3>
    <p>Women with HIV have a higher risk of:</p>
    <ul>
      <li><strong>Human Papillomavirus (HPV) infection:</strong> More persistent and difficult to clear</li>
      <li><strong>Cervical dysplasia:</strong> Abnormal cell changes on the cervix</li>
      <li><strong>Cervical cancer:</strong> Higher risk of progression to cancer</li>
    </ul>

    <p>Regular Pap smears and cervical exams are especially important for women with HIV.</p>

    <h2>Advanced HIV Symptoms</h2>
    <p>Without treatment, HIV progresses and symptoms become more severe:</p>
    <ul>
      <li><strong>Persistent, unexplained fatigue:</strong> Even after adequate rest</li>
      <li><strong>Rapid weight loss:</strong> More than 10% of body weight</li>
      <li><strong>Recurring fever or night sweats:</strong> Frequent episodes</li>
      <li><strong>Persistent lymphadenopathy:</strong> Swollen lymph nodes for months</li>
      <li><strong>Chronic diarrhea:</strong> Lasting more than a week</li>
      <li><strong>Opportunistic infections:</strong> Infections that wouldn't normally affect healthy people</li>
      <li><strong>Neurological problems:</strong> Memory loss, depression, or confusion</li>
      <li><strong>Skin problems:</strong> Rashes, lesions, or skin infections</li>
    </ul>

