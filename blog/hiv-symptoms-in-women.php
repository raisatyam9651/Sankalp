    <h2>HIV Prevention: PrEP and PEP</h2>
    <div class="tip-box">
      <h4>Prevention Options Available:</h4>
      <p><strong>PrEP (Pre-Exposure Prophylaxis):</strong> A daily pill for people at high risk of HIV. When taken as prescribed, PrEP is highly effective (over 99%) at preventing HIV infection.</p>
    </div>

    <h3>PrEP - For Before Potential Exposure:</h3>
    <ul>
      <li>Daily pill that should be taken consistently</li>
      <li>Recommended for high-risk individuals</li>
      <li>Must be prescribed by a healthcare provider</li>
      <li>Requires regular HIV testing and monitoring</li>
    </ul>

    <h3>PEP - For Emergency Situations:</h3>
    <ul>
      <li>Emergency medicine taken after potential exposure</li>
      <li>Must be started within 72 hours (ideally within 24 hours)</li>
      <li>Taken for 28 days</li>
      <li>Not a substitute for regular prevention methods</li>
    </ul>

    <h2>HIV Treatment and Care</h2>
    <p>HIV is treated with antiretroviral therapy (ART), which is a combination of medicines that work together to control the virus:</p>
    <ul>
      <li><strong>Controls viral load:</strong> Reduces the amount of HIV in the body</li>
      <li><strong>Boosts immune system:</strong> Allows CD4 cells to recover</li>
      <li><strong>Prevents progression:</strong> Stops HIV from advancing to AIDS</li>
      <li><strong>Reduces transmission risk:</strong> Undetectable viral load means untransmittable (U=U)</li>
    </ul>

    <div class="info-box">
      <h4>U = U Campaign:</h4>
      <p>Undetectable = Untransmittable. When a person with HIV takes their medication consistently and achieves an undetectable viral load, they cannot transmit HIV to their sexual partners. This is a powerful message that encourages treatment adherence.</p>
    </div>

    <h2>Living with HIV: Key Points for Women</h2>
    <ul>
      <li><strong>Start treatment early:</strong> The sooner you start ART, the better for your long-term health</li>
      <li><strong>Take medications consistently:</strong> Missed doses can lead to drug resistance</li>
      <li><strong>Regular check-ups:</strong> Stay connected with your healthcare provider</li>
      <li><strong>Mental health support:</strong> Seek counseling if needed - HIV diagnosis can be emotionally challenging</li>
      <li><strong>Support groups:</strong> Connect with others living with HIV for support</li>
      <li><strong>Disclose safely:</strong> Only tell people you trust and who support you</li>
      <li><strong>Pregnancy planning:</strong> Work with your doctor if planning pregnancy</li>
    </ul>

    <h2>Frequently Asked Questions (FAQs)</h2>

    <div class="faq-item">
      <h4>What are the first signs of HIV in women?</h4>
      <p>The first signs usually appear 2-4 weeks after exposure and may include fever, fatigue, sore throat, swollen lymph nodes, headache, muscle aches, and sometimes a rash. These flu-like symptoms are often mistaken for other infections. However, the only way to confirm HIV is through testing.</p>
    </div>

    <div class="faq-item">
      <h4>How long does it take for HIV symptoms to appear in women?</h4>
      <p>HIV symptoms can appear within 2-4 weeks after exposure for some women, while others may not show any symptoms for years. On average, without treatment, HIV progresses to symptomatic stage within 5-10 years. Early testing is crucial because even without symptoms, you can transmit the virus to others.</p>
    </div>

    <div class="faq-item">
      <h4>Can HIV be transmitted through oral sex?</h4>
      <p>While the risk is lower than vaginal or anal sex, HIV can still be transmitted through oral sex, especially if there are cuts, sores, or bleeding gums. Using condoms or dental dams during oral sex can reduce the risk. If you have concerns, discuss with a healthcare provider about prevention options like PrEP.</p>
    </div>

    <div class="faq-item">
      <h4>Is HIV curable?</h4>
      <p>Currently, there is no cure for HIV. However, with proper treatment using antiretroviral therapy (ART), HIV can be controlled effectively. People with HIV who take their medication consistently can achieve an undetectable viral load and live long, healthy lives while preventing transmission to others.</p>
    </div>

    <div class="faq-item">
      <h4>Can I get pregnant if I have HIV?</h4>
      <p>Yes, women with HIV can have healthy pregnancies. With proper medical care and ART treatment, the risk of transmitting HIV to the baby can be reduced to less than 1%. It's important to work closely with healthcare providers who specialize in HIV care and pregnancy.</p>
    </div>

    <div class="faq-item">
      <h4>How accurate are HIV tests?</h4>
      <p>Modern HIV tests are highly accurate. Antibody tests have sensitivity rates of over 99%. However, there is a "window period" - the time between exposure and when a test can detect the virus. During this period, a person can be infected but test negative. If you had a recent exposure, you may need follow-up testing.</p>
    </div>

    <div class="faq-item">
      <h4>Where can I get tested for HIV?</h4>
      <p>HIV testing is available at hospitals, clinics, community health centers, and through home testing kits. Many places offer free or confidential testing. You can also find testing locations through local health departments or online resources. Remember, early detection saves lives.</p>
    </div>

    <div class="faq-item">
      <h4>Does HIV affect women differently than men?</h4>
      <p>Women may experience some unique symptoms such as more frequent vaginal infections, menstrual changes, and higher risk of certain conditions like PID and cervical cancer. Women may also respond differently to some HIV medications. However, with proper treatment, women can achieve the same positive outcomes as men.</p>
    </div>

