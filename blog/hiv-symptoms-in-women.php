    <p class="lead">HIV (Human Immunodeficiency Virus) affects millions of women worldwide. Early detection is crucial for effective treatment and preventing transmission.</p>

    <blockquote>"HIV is not a death sentence anymore. With early diagnosis and proper treatment, women with HIV can live long, healthy lives."</blockquote>

    <h2>How HIV Spreads</h2>
    <p>HIV spreads through:</p>
    <ul>
      <li>Unprotected sexual contact</li>
      <li>Sharing needles or syringes</li>
      <li>From mother to child during pregnancy, birth, or breastfeeding</li>
    </ul>

    <h2>Early Symptoms in Women</h2>
    <p>2-4 weeks after exposure, some women experience flu-like symptoms:</p>
    <ul>
      <li>Fever (often high, 100-102°F)</li>
      <li>Fatigue and weakness</li>
      <li>Sore throat</li>
      <li>Swollen lymph nodes</li>
      <li>Headache</li>
      <li>Muscle aches and joint pain</li>
      <li>Night sweats</li>
    </ul>

    <h2>Women-Specific HIV Symptoms</h2>
    <h3>1. Vaginal Infections</h3>
    <p>Women with HIV are more prone to yeast infections, bacterial vaginosis, and recurring UTIs.</p>

    <h3>2. Menstrual Changes</h3>
    <p>HIV can cause irregular periods, lighter or heavier bleeding than normal.</p>

    <h3>3. Pelvic Inflammatory Disease (PID)</h3>
    <p>Women with HIV may have more severe and frequent PID episodes.</p>

    <h2>Testing for HIV</h2>
    <p><strong>Who should get tested?</strong> Everyone between 15-65 years should get tested at least once.</p>

    <div class="alert-warning">
      <strong>PrEP and PEP:</strong>
      <ul>
        <li><strong>PrEP:</strong> Daily pill for high-risk individuals to prevent HIV</li>
        <li><strong>PEP:</strong> Emergency medicine taken within 72 hours after potential exposure</li>
      </ul>
    </div>

    <h2>Prevention Tips</h2>
    <ul>
      <li>Use condoms correctly every time you have sex</li>
      <li>Get tested regularly with your partner</li>
      <li>Don't share needles</li>
      <li>Consider PrEP if you're at high risk</li>
    </ul>

