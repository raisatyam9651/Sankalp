<h2>तुरंत राहत के लिए असरदार घरेलू उपाय (Home Remedies)</h2>
<p>यदि खुजली किसी गंभीर मेडिकल स्थिति के कारण नहीं है, तो आप इन घरेलू नुस्खों से तुरंत आराम पा सकते हैं:</p>

<h3>1. ठंडे पानी और ओटमील (Oatmeal) का इस्तेमाल</h3>
<p>कोलाइडल ओटमील (बारीक पिसा हुआ दलिया) त्वचा की जलन और खुजली को शांत करने के लिए बेहतरीन है। एक टब ठंडे या हल्के गुनगुने पानी में एक कप कोलाइडल ओटमील मिलाएं और अपने पैरों को 15-20 मिनट तक उसमें भिगोएं। <strong>ध्यान रहे:</strong> कभी भी तेज गर्म पानी का उपयोग न करें, इससे त्वचा का रूखापन और खुजली और बढ़ जाएगी।</p>

<h3>2. सेब का सिरका (Apple Cider Vinegar)</h3>
<p>सेब के सिरके में एंटी-बैक्टीरियल और एंटी-फंगल गुण होते हैं। यह एथलीट्स फुट (फंगल इन्फेक्शन) में बहुत कारगर है। एक टब पानी में आधा कप कच्चा सेब का सिरका मिलाएं और पैरों को 15 मिनट के लिए डुबोएं। इसके बाद पैरों को अच्छी तरह सुखा लें। (यदि त्वचा पर दरारें या खुले घाव हैं तो इसका इस्तेमाल न करें)।</p>

<h3>3. एलोवेरा और नारियल का तेल</h3>
<p>सूखी त्वचा के लिए यह सबसे अच्छा मॉइस्चराइज़र है। नहाने के तुरंत बाद (जब त्वचा हल्की नम हो), शुद्ध वर्जिन नारियल तेल या ताज़ा एलोवेरा जेल पैरों और एड़ियों पर अच्छी तरह मलें। यह नमी को त्वचा के अंदर लॉक कर देता है और खुजली पैदा करने वाले रूखेपन को खत्म करता है।</p>

<h3>4. बेकिंग सोडा (Baking Soda) पेस्ट</h3>
<p>बेकिंग सोडा त्वचा के pH स्तर को संतुलित करता है। 2-3 चम्मच बेकिंग सोडा में थोड़ा पानी मिलाकर एक गाढ़ा पेस्ट बनाएं। इसे खुजली वाली जगह (विशेषकर जहां दाने हों) पर लगाएं और 10 मिनट बाद ठंडे पानी से धो लें।</p>

<h2>पैरों की देखभाल और बचाव (Prevention Tips)</h2>
<ul>
<li><strong>पैरों को पूरी तरह सुखाएं:</strong> नहाने के बाद तौलिए से पैरों को, खासकर उंगलियों के बीच की जगह को, अच्छी तरह पोंछकर सुखाएं। फंगस हमेशा नमी में पनपता है।</li>
<li><strong>सही मोजे और जूते चुनें:</strong> हमेशा 100% सूती (Cotton) या ऊनी मोजे पहनें जो पसीना सोख सकें। नायलॉन या सिंथेटिक मोजे पहनने से बचें। जूते ऐसे पहनें जिनमें हवा का संचार (Ventilation) हो सके।</li>
<li><strong>रोज मोजे बदलें:</strong> यदि आपके पैरों में बहुत पसीना आता है, तो दिन में दो बार मोजे बदलें और जूतों के अंदर एंटी-फंगल पाउडर छिड़कें।</li>
<li><strong>नंगे पैर न चलें:</strong> सार्वजनिक स्विमिंग पूल, जिम के लॉकर रूम या बाथरूम में नंगे पैर न चलें। हमेशा अपनी चप्पल (Flip-flops) का इस्तेमाल करें।</li>
</ul>

