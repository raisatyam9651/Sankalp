<h2>पैर में खुजली के प्रमुख कारण (Causes of Itchy Feet)</h2>
<p>पैरों में खुजली के पीछे कई त्वचा संबंधी और अंदरूनी (सिस्टमिक) कारण हो सकते हैं:</p>

<h3>1. फंगल इन्फेक्शन (Athlete's Foot / Tinea Pedis)</h3>
<p>पैरों में खुजली का सबसे आम कारण फंगल इन्फेक्शन है, जिसे 'एथलीट्स फुट' कहा जाता है। यह फंगस नम और गर्म वातावरण (जैसे पसीने से भीगे मोजे और जूते) में तेजी से पनपता है। इसके कारण पैरों की उंगलियों के बीच की त्वचा लाल हो जाती है, छिलने लगती है और उसमें अत्यधिक तेज खुजली और जलन होती है।</p>

<h3>2. सूखी त्वचा (Xerosis / Dry Skin)</h3>
<p>सर्दियों के मौसम में या ज्यादा गर्म पानी से नहाने के कारण पैरों की प्राकृतिक नमी (Natural oils) खत्म हो जाती है। जब त्वचा बहुत ज्यादा रूखी हो जाती है, तो उसमें दरारें (Cracks) पड़ने लगती हैं और खिंचाव के कारण तेज खुजली उत्पन्न होती है। बुजुर्गों में त्वचा का पतला और रूखा होना खुजली का एक बड़ा कारण है।</p>

<h3>3. संपर्क डर्मेटाइटिस (Contact Dermatitis / Allergy)</h3>
<p>यह एक एलर्जिक रिएक्शन है। जब आपके पैर किसी ऐसे रसायन (Chemical) के संपर्क में आते हैं जिससे आपको एलर्जी है—जैसे नायलॉन के मोजे, जूतों में इस्तेमाल होने वाला गोंद (Glue), कपड़े धोने का डिटरजेंट, या कोई नई क्रीम—तो त्वचा पर लाल चकत्ते (Rashes) उभर आते हैं और भयंकर खुजली होती है।</p>

<h3>4. मधुमेह (Diabetes) और न्यूरोपैथी</h3>
<p>जिन लोगों का ब्लड शुगर लेवल लंबे समय तक हाई रहता है, उनकी नसों को नुकसान पहुंचता है जिसे 'पेरिफेरल न्यूरोपैथी' कहते हैं। इसमें पैरों में सुन्नपन और झनझनाहट के साथ-साथ एक अजीब सी खुजली (जो त्वचा के अंदर महसूस होती है) होती है। साथ ही, डायबिटीज में त्वचा रूखी हो जाती है और फंगल इन्फेक्शन का खतरा कई गुना बढ़ जाता है।</p>

<h3>5. किडनी और लिवर की बीमारियां</h3>
<p>क्रोनिक किडनी डिजीज (CKD) या लिवर सिरोसिस होने पर शरीर से अपशिष्ट पदार्थ (Toxins) और पित्त (Bile salts) ठीक से बाहर नहीं निकल पाते। ये टॉक्सिन्स रक्त के माध्यम से त्वचा के नीचे जमा होने लगते हैं, जिससे पूरे शरीर में (विशेषकर पैरों और हाथों के तलवों में) गंभीर खुजली होती है, जिसे 'यूरेमिक प्रुरिटस' कहा जाता है।</p>

<h3>6. सोरायसिस (Psoriasis) और एक्जिमा</h3>
<p>ये दोनों ही क्रोनिक त्वचा रोग हैं। एक्जिमा में त्वचा पर लाल, सूजे हुए और पानी भरे दाने निकल आते हैं जिनमें तीव्र खुजली होती है। वहीं, पामोप्लान्टर सोरायसिस (Palmoplantar Psoriasis) में पैरों के तलवों पर मोटी, लाल और पपड़ीदार त्वचा बन जाती है जो फटने पर दर्द और खुजली करती है।</p>

