    <h3>1. नमक और पानी</h3>
    <p>गुनगुने पानी में नमक डालकर पैर भिगोएं। 15-20 मिनट तक पैरों को पानी में रखें। यह उपाय फंगल इंफेक्शन में विशेष रूप से प्रभावी है। नमक फंगस को मारने में मदद करता है।</p>

    <h3>2. मॉइस्चराइज़र लगाना</h3>
    <p>नहाने के तुरंत बाद, जब त्वचा अभी गीली है, पैरों पर मॉइस्चराइज़र लगाएं। इससे नमी त्वचा में रहती है। ग्लिसरीन युक्त क्रीम सबसे अच्छी होती है।</p>

    <h3>3. एलोवेरा जेल</h3>
    <p>एलोवेरा जेल त्वचा को शांत करता है और खुजली कम करता है। शुद्ध एलोवेरा जेल प्रभावी होता है। प्रतिदिन 2-3 बार लगाएं।</p>

    <h3>4. ठंडी सेंक</h3>
    <p>खुजली वाली जगह पर ठंडे पानी से कपड़ा रखें। बर्फ से भरा कपड़ा भी लगा सकते हैं। 10-15 मिनट तक रखें और दोहराएं।</p>

    <h3>5. सिरके का उपाय</h3>
    <p>सेब के सिरके को पानी में मिलाकर पैर भिगोएं। सिरका फंगल इंफेक्शन को रोकने में मदद करता है। 1:4 अनुपात में पानी में मिलाएं।</p>

    <h3>6. नारियल तेल</h3>
    <p>वर्जिन नारियल तेल पैरों पर लगाएं। यह मॉइस्चराइज़र की तरह काम करता है और एंटीफंगल गुण भी रखता है। रात को सोने से पहले लगाएं।</p>

