    <h3>1. सूखी त्वचा (Dry Skin / Xerosis)</h3>
    <p>सबसे आम कारण है। सर्दियों में या ज्यादा गर्म पानी से नहाने से त्वचा सूख जाती है। सूखी त्वचा में खुजली अधिक होती है क्योंकि त्वचा की बाहरी परत (दंड) टूट जाती है। सर्दियों में हवा भी शुष्क होती है जो त्वचा की नमी को और कम करती है।</p>
    <p><strong>लक्षण:</strong> त्वचा खुरदरी महसूस होती है, सफेद चकत्ते दिख सकते हैं, त्वचा में दरारें आ सकती हैं।</p>

    <h3>2. एग्जीमा (Eczema / Dermatitis)</h3>
    <p>त्वचा की एक सूजन संबंधी समस्या है जिसमें खुजली के साथ लाल चकत्ते और त्वचा का पपड़ी उतरना हो सकता है। एटोपिक डर्मेटाइटिस एक प्रकार का एक्जीमा है जो अक्सर पैरों में होता है।</p>
    <p><strong>लक्षण:</strong> लाल चकत्ते, सूजन, त्वचा का पपड़ी उतरना, तेज खुजली जो रात को बढ़ती है।</p>

    <h3>3. फंगल इंफेक्शन (Athlete's Foot / Tinea Pedis)</h3>
    <p>पैर की उंगलियों के बीच फंगल इंफेक्शन हो सकता है जिसमें तेज खुजली, त्वचा का छिलना और जलन होती है। यह संक्रमण जूतों में रहने वाले फंगस से फैलता है।</p>
    <p><strong>लक्षण:</strong> उंगलियों के बीच खुजली और लालिमा, त्वचा का सफेद और छिला हुआ दिखना, तेज दर्ददायक खुजली।</p>

    <h3>4. सोरायसिस (Psoriasis)</h3>
    <p>सोरायसिस एक ऑटोइम्यून बीमारी है जिसमें त्वचा की कोशिकाएं बहुत तेजी से बनती हैं। इससे त्वचा पर महीन, चांदी जैसे चकत्ते बनते हैं।</p>
    <p><strong>लक्षण:</strong> चांदी जैसे चकत्ते, सूखी और मोटी त्वचा, खुजली और जलन।</p>

    <h3>5. एलर्जी प्रतिक्रिया</h3>
    <p>कुछ सामग्री, साबुन, क्रीम, या जूतों की सामग्री से एलर्जी हो सकती है। कुछ डिटर्जेंट में भी फैब्रिक सॉफ्टनर होता है जो एलर्जी कर सकता है।</p>
    <p><strong>लक्षण:</strong> खुजली, लाल चकत्ते, सूजन।</p>

    <h3>6. परजीवी (Scabies)</h3>
    <p>स्कैबीज एक त्वचा संबंधी रोग है जो छोटे परजीवी (mite) के काटने से होता है। ये परजीवी त्वचा में छोटे-छोटे रास्ते बनाते हैं जिनमें खुजली होती है।</p>
    <p><strong>लक्षण:</strong> तेज खुजली (रात को बढ़ती है), उंगलियों के बीच और कलाई पर छोटे-छोटे घाव।</p>

    <h3>7. डायबिटीज</h3>
    <p>मधुमेह (डायबिटीज) में पैरों में खुजली हो सकती है क्योंकि रक्त में शर्करा का स्तर बढ़ने से त्वचा सूखती है और संक्रमण का खतरा बढ़ जाता है।</p>
    <p><strong>लक्षण:</strong> पैरों में खुजली, सुन्नपन, घाव धीरे से ठीक होना।</p>

