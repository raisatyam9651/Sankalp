    <h2>Common Causes of Imbalance</h2>
    <p>Several factors can disrupt hormonal balance, including chronic stress, poor diet, certain medical conditions like PCOS (Polycystic Ovary Syndrome), and natural transitions like perimenopause and menopause.</p>

