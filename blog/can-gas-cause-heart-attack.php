  <li><strong>Manage stress:</strong> Chronic stress harms your heart</li>
  <li><strong>Get adequate sleep:</strong> 7-9 hours per night</li>
  <li><strong>Schedule regular checkups:</strong> Especially if you have risk factors</li>
</ul>

