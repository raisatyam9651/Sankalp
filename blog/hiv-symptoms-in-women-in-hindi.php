<h2>एचआईवी संक्रमण के विभिन्न चरण (Stages of HIV)</h2>
<p>एचआईवी रातोंरात गंभीर रूप नहीं लेता है। यह धीरे-धीरे शरीर में फैलता है और इसके तीन मुख्य चरण होते हैं:</p>
<ol>
<li><strong>एक्यूट इंफेक्शन (Acute HIV Infection):</strong> जोखिम भरे संपर्क के 2 से 4 सप्ताह बाद, शरीर वायरस के प्रति प्रतिक्रिया करता है। इस दौरान फ्लू जैसे लक्षण दिखाई देते हैं। इस चरण में रक्त में वायरस का स्तर बहुत अधिक होता है।</li>
<li><strong>क्लीनिकल लैटेंसी (Clinical Latency / Chronic HIV):</strong> इस चरण में वायरस शरीर में गुणा करता रहता है लेकिन बहुत धीमी गति से। बिना इलाज के यह चरण 10 साल या उससे अधिक समय तक चल सकता है। इस दौरान कोई स्पष्ट लक्षण दिखाई नहीं देते, लेकिन व्यक्ति दूसरों को संक्रमित कर सकता है।</li>
<li><strong>एड्स (AIDS):</strong> यह एचआईवी का सबसे गंभीर चरण है। जब शरीर का इम्यून सिस्टम पूरी तरह से नष्ट हो जाता है और CD4 कोशिकाओं की संख्या 200 से नीचे आ जाती है, तब मरीज एड्स का शिकार हो जाता है।</li>
</ol>

<h2>महिलाओं में एचआईवी के शुरुआती लक्षण (Early Symptoms)</h2>
<p>संक्रमण के 2 से 4 सप्ताह बाद, कई महिलाओं को हल्के फ्लू (Flu) जैसे लक्षण महसूस होते हैं। यह शरीर का वायरस से लड़ने का प्राकृतिक तरीका है। इन लक्षणों को 'एक्यूट रेट्रोवायरल सिंड्रोम' (ARS) कहा जाता है:</p>
<ul>
<li><strong>बुखार और ठंड लगना:</strong> हल्का से मध्यम बुखार जो कई दिनों तक बना रह सकता है (अक्सर 100-102°F के बीच)।</li>
<li><strong>अत्यधिक थकान (Fatigue):</strong> पर्याप्त आराम करने के बावजूद शरीर में ऊर्जा की कमी महसूस होना और बिना काम किए थक जाना।</li>
<li><strong>सूजी हुई लिम्फ नोड्स (Swollen Lymph Nodes):</strong> गर्दन, बगल (Armpits), और कमर (Groin) की ग्रंथियों (लिम्फ नोड्स) में सूजन आना, जो अक्सर दर्दनाक होता है।</li>
<li><strong>त्वचा पर चकत्ते (Skin Rashes):</strong> शरीर के ऊपरी हिस्से, छाती या पेट पर लाल रंग के दाने या चकत्ते निकलना जिनमें खुजली नहीं होती है।</li>
<li><strong>गले में खराश और मुंह के छाले:</strong> निगलने में दर्द होना और गले में खराश बने रहना।</li>
<li><strong>रात में पसीना आना (Night Sweats):</strong> सोते समय अत्यधिक पसीना आना जिससे कपड़े तक भीग जाएं।</li>
<li><strong>मांसपेशियों और जोड़ों में दर्द:</strong> पूरे शरीर में दर्द (Body ache) महसूस होना।</li>
</ul>

<h2>महिलाओं के लिए विशिष्ट लक्षण (Women-Specific Symptoms)</h2>
<p>चूंकि एचआईवी सीधे तौर पर इम्यून सिस्टम को कमजोर करता है, इसलिए यह महिलाओं के प्रजनन स्वास्थ्य (Reproductive health) को गहराई से प्रभावित करता है। महिलाओं में कुछ ऐसे लक्षण दिखाई देते हैं जो पुरुषों में नहीं होते:</p>

<h3>1. बार-बार योनि संक्रमण (Vaginal Yeast Infections)</h3>
<p>हालांकि यीस्ट इन्फेक्शन महिलाओं में आम है, लेकिन एचआईवी से संक्रमित महिलाओं में यह बहुत बार-बार होता है। योनि में खुजली, जलन, दर्द और गाढ़ा सफेद स्राव (Discharge) होता है। एचआईवी पॉजिटिव महिलाओं में ये संक्रमण सामान्य दवाइयों से आसानी से ठीक नहीं होते हैं।</p>

<h3>2. मासिक धर्म में बदलाव (Menstrual Changes)</h3>
<p>एचआईवी के कारण महिलाओं के पीरियड्स (मासिक धर्म) अनियमित हो सकते हैं। पीरियड्स का बहुत हल्का या बहुत भारी होना, समय पर न आना, या प्रीमेन्स्ट्रुअल सिंड्रोम (PMS) के लक्षणों का बहुत अधिक बढ़ जाना इसके संकेत हो सकते हैं।</p>

<h3>3. पेल्विक इन्फ्लेमेटरी डिजीज (PID)</h3>
<p>पीआईडी (Pelvic Inflammatory Disease) गर्भाशय, फैलोपियन ट्यूब और ओवरी का एक संक्रमण है। महिलाओं में जिनकी रोग प्रतिरोधक क्षमता कम होती है, उनमें पीआईडी बार-बार होता है और इसका इलाज करना काफी मुश्किल हो जाता है। इसमें पेट के निचले हिस्से में तेज दर्द होता है।</p>

<h3>4. एचपीवी और सर्वाइकल कैंसर का खतरा</h3>
<p>एचआईवी पॉजिटिव महिलाओं में 'ह्यूमन पेपिलोमावायरस' (HPV) संक्रमण होने का खतरा काफी अधिक होता है। एचपीवी गर्भाशय ग्रीवा (Cervix) की कोशिकाओं में बदलाव कर सकता है, जिससे सर्वाइकल कैंसर का जोखिम कई गुना बढ़ जाता है। इसलिए पैप स्मीयर (Pap Smear) टेस्ट कराना बहुत जरूरी है।</p>

<h2>एचआईवी कैसे फैलता है और कैसे नहीं?</h2>
<p>जागरूकता की कमी के कारण समाज में एचआईवी को लेकर कई भ्रांतियां हैं। यह जानना जरूरी है कि यह कैसे फैलता है:</p>
<ul>
<li><strong>फैलता है:</strong> असुरक्षित यौन संबंध (कंडोम के बिना), संक्रमित सुई या सिरिंज का साझा उपयोग, और संक्रमित मां से गर्भावस्था, जन्म या स्तनपान के दौरान बच्चे को।</li>
<li><strong>नहीं फैलता है:</strong> हाथ मिलाने से, गले लगने से, साथ में खाना खाने से, मच्छर के काटने से, या एक ही शौचालय (Toilet) का इस्तेमाल करने से एचआईवी बिल्कुल नहीं फैलता।</li>
</ul>

