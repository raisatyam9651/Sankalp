    <p class="lead">AIDS (Acquired Immunodeficiency Syndrome) is the most advanced stage of HIV infection. Understanding how HIV progresses to AIDS in women, and the symptoms that define this stage, is crucial for obtaining life-saving treatment.</p>

    <h2>The Progression from HIV to AIDS</h2>
    <p>HIV infects and destroys CD4 cells, which are part of the immune system. When the immune system is significantly weakened, the body can no longer fight off "opportunistic infections" and certain cancers. At this point, the diagnosis becomes AIDS.</p>

    <h3>Common Symptoms of Advanced Infection in Women</h3>
    <p>As the immune system declines, women may experience:</p>
    <ul>
      <li><strong>Rapid Weight Loss:</strong> Often called "wasting syndrome," characterized by unintentional loss of body weight.</li>
      <li><strong>Chronic Diarrhea:</strong> Persistent digestive issues lasting more than a week.</li>
      <li><strong>Prolonged Swelling of Lymph Nodes:</strong> In the armpits, groin, or neck.</li>
      <li><strong>Severe Night Sweats and Recurring Fever:</strong> Signs of the body's struggle against infections.</li>
      <li><strong>Persistent Oral or Vaginal Yeast Infections:</strong> Infections that occur frequently and are difficult to treat.</li>
    </ul>

    <div class="risk-box">
      <h4>Opportunistic Infections</h4>
      <p>AIDS-defining conditions include pneumonia (Pneumocystis jirovecii), tuberculosis, certain types of meningitis, and cancers like Kaposi's sarcoma. Women with AIDS are also at a significantly higher risk for advanced cervical cancer.</p>
    </div>

    <h2>Unique Health Risks for Women</h2>
    <p>Hormonal changes and reproductive health are deeply impacted by advanced HIV. Women may experience severe menstrual irregularities and increased susceptibility to HPV (Human Papillomavirus) and subsequent cervical health issues.</p>

    <h2>Testing and Modern Care</h2>
    <p>The transition from HIV to AIDS can be prevented with early diagnosis and consistent Antiretroviral Therapy (ART). At Sankalp Hospital, we provide confidential testing, comprehensive counseling, and advanced treatment to help patients manage their condition effectively.</p>

