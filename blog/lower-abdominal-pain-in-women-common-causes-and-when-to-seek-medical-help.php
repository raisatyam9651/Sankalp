    <p class="lead">Lower abdominal pain (pelvic pain) is a frequent concern for women. Because the lower abdomen contains many different organs—including the reproductive, urinary, and digestive systems—the cause of pain can vary widely.</p>

    <h2>Common Gynecological Causes</h2>
    <p>Many cases of lower abdominal pain are related to the female reproductive system:</p>
    <ul>
      <li><strong>Menstrual Cramps (Dysmenorrhea):</strong> Sharp or throbbing pain that occurs just before or during a period.</li>
      <li><strong>Ovulation Pain (Mittelschmerz):</strong> A one-sided, dull ache felt mid-cycle.</li>
      <li><strong>Ovarian Cysts:</strong> Fluid-filled sacs that can cause dull or sharp pain if they rupture or grow large.</li>
      <li><strong>Endometriosis:</strong> Chronic pain caused by uterine tissue growing outside the uterus.</li>
      <li><strong>Pelvic Inflammatory Disease (PID):</strong> An infection of the reproductive organs that requires prompt medical attention.</li>
    </ul>

    <h2>Digestive and Urinary Causes</h2>
    <p>Sometimes the pain originates from other systems:</p>
    <ul>
      <li><strong>Urinary Tract Infection (UTI):</strong> Pain or pressure in the lower abdomen along with a burning sensation during urination.</li>
      <li><strong>Constipation or Gas:</strong> Common causes of cramping and discomfort in the lower abdomen.</li>
      <li><strong>Irritable Bowel Syndrome (IBS):</strong> Chronic abdominal pain associated with changes in bowel habits.</li>
      <li><strong>Appendicitis:</strong> Typically starts near the navel and moves to the lower right abdomen. This is a medical emergency.</li>
    </ul>

