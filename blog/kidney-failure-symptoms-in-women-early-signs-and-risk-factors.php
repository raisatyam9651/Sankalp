    <p class="lead">Chronic Kidney Disease (CKD) often progresses silently, with symptoms appearing only when the kidneys are significantly damaged. For women, awareness of early signs is crucial for timely intervention and preserving kidney function.</p>

    <h2>Early Warning Signs in Women</h2>
    <p>Many early symptoms of kidney failure are non-specific and can be easily overlooked or attributed to other causes.</p>

    <h3>1. Persistent Fatigue and Weakness</h3>
    <p>Kidneys produce a hormone called erythropoietin that tells your body to make red blood cells. When kidneys fail, they make less of this hormone, leading to anemia, which causes constant tiredness and lack of energy.</p>

    <h3>2. Swelling (Edema)</h3>
    <p>When kidneys can't remove excess fluid and salt, it builds up in the body. You may notice swelling in your feet, ankles, or hands. Puffiness around the eyes, especially in the morning, is also a common sign.</p>

    <h3>3. Changes in Urination</h3>
    <p>Pay attention to any changes in your bathroom habits. Signs include urinating more or less often, urine that looks foamy or bubbly (indicating protein leakage), or blood in the urine.</p>

    <h3>4. Skin Itchiness and Dryness</h3>
    <p>Kidneys remove waste from the bloodstream. When they fail, waste builds up in the blood, causing severe itching and dry, irritated skin.</p>

    <div class="risk-box">
      <h4>Who is at Higher Risk?</h4>
      <p>Women with a history of diabetes, high blood pressure, or a family history of kidney disease are at the highest risk. Additionally, certain autoimmune diseases like Lupus are more common in women and can lead to kidney damage.</p>
    </div>

    <h2>Risk Factors Specific to Women</h2>
    <ul>
      <li><strong>Pregnancy Complications:</strong> Conditions like preeclampsia can increase the risk of developing kidney disease later in life.</li>
      <li><strong>Autoimmune Diseases:</strong> Women are more likely to have conditions like Lupus or Rheumatoid Arthritis, which can affect kidney function.</li>
      <li><strong>Frequent UTIs:</strong> Recurrent or untreated urinary tract infections can lead to kidney scarring and damage.</li>
    </ul>

    <h2>Protecting Your Kidneys</h2>
    <p>Prevention and early detection are your best defenses. Regular screenings for blood pressure and blood sugar are essential, especially if you have risk factors.</p>
    <p><strong>Healthy Habits:</strong> Maintain a balanced diet low in processed foods, stay active, and avoid the overuse of over-the-counter pain medications (NSAIDs), which can damage kidneys over time.</p>

