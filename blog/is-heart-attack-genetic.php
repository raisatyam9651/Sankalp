<h2>क्या Heart Attack आनुवंशिक (Genetic) होता है?</h2>
<p>विज्ञान कहता है कि हाँ, <strong>Genetic Heart Disease Risk</strong> वास्तविक है। यदि आपके माता-पिता, भाई-बहन या दादा-दादी को जल्दी उम्र (55 साल से पहले पुरुषों में, 65 साल से पहले महिलाओं में) में <strong>हृदयाघात (Heart Attack)</strong> या <strong>कोरोनरी हार्ट डिजीज (Coronary Artery Disease)</strong> हुआ है, तो आपमें भी इसका खतरा 2 से 3 गुना अधिक हो सकता है।</p>

<h3>जेनेटिक फैक्टर्स कैसे काम करते हैं?</h3>
<p>आपके जीन्स (Genes) कई चीजों को प्रभावित करते हैं जो हृदय स्वास्थ्य के लिए महत्वपूर्ण हैं:</p>
<ul>
<li><strong>कोलेस्ट्रॉल का स्तर:</strong> कुछ जीन्स LDL (खराब) कोलेस्ट्रॉल को बढ़ाते हैं और HDL (अच्छे) कोलेस्ट्रॉल को कम करते हैं।</li>
<li><strong>रक्तचाप (Blood Pressure):</strong> हाइपरटेंशन (Hypertension) की प्रवृत्ति परिवारों में चलती है।</li>
<li><strong>चयापचय (Metabolism):</strong> मधुमेह (Diabetes) और मोटापे (Obesity) की प्रवृत्ति भी आनुवंशिक हो सकती है।</li>
<li><strong>रक्त का थक्का बनना (Clotting):</strong> कुछ जेनेटिक म्यूटेशन रक्त के थक्के बनने की संभावना बढ़ाते हैं।</li>
</ul>

<h2>Heart Attack के प्रमुख जोखिम कारक (Risk Factors)</h2>
<p><strong>Heart Attack</strong> एक ही कारण से नहीं होता — यह कई जोखिम कारकों (Risk Factors) का संयुक्त प्रभाव है:</p>

<h3>1. पारिवारिक इतिहास (Family History) — Non-Modifiable</h3>
<p>जैसा कि पहले बताया, यदि आपके परिवार में किसी को <strong>Early Heart Attack</strong> हुआ है, तो आपका जोखिम अधिक है। यह एकमात्र ऐसा कारक है जिसे हम बदल नहीं सकते।</p>

<h3>2. उम्र और लिंग (Age & Gender) — Non-Modifiable</h3>
<p>पुरुषों में 45+ और महिलाओं में 55+ वर्ष की उम्र के बाद <strong>Cardiac Risk</strong> बढ़ जाता है। महिलाओं को <strong>Menopause</strong> के बाद एस्ट्रोजन (Estrogen) के स्तर में कमी के कारण हृदय रोग का खतरा बढ़ता है।</p>

<h3>3. उच्च रक्तचाप (Hypertension)</h3>
<p>लगातार <strong>High Blood Pressure</strong> धमनियों (Arteries) की दीवारों को नुकसान पहुंचाता है और <strong>Coronary Artery Disease</strong> का कारण बनता है। नियमित रूप से <strong>Blood Pressure</strong> की जांच करें और इसे 120/80 mmHg से नीचे रखें।</p>

<h3>4. उच्च कोलेस्ट्रॉल (High Cholesterol)</h3>
<p><strong>LDL Cholesterol</strong> (खराब कोलेस्ट्रॉल) धमनियों में प्लाक (Plaque) जमा करता है, जिससे <strong>Atherosclerosis</strong> होती है — यही <strong>Heart Attack</strong> का मुख्य कारण है।</p>

<h3>5. मधुमेह (Diabetes)</h3>
<p><strong>Diabetes</strong> से पीड़ित लोगों में <strong>Heart Attack</strong> का खतरा 2-4 गुना अधिक होता है। <strong>Diabetes</strong> रक्त वाहिकाओं (Blood Vessels) को भी नुकसान पहुंचाता है और थक्के बनने की प्रवृत्ति बढ़ाता है।</p>

<h3>6. मोटापा (Obesity)</h3>
<p><strong>Obesity</strong> <strong>Hypertension</strong>, <strong>Diabetes</strong> और <strong>High Cholesterol</strong> तीनों को बढ़ावा देता है — एक साथ तीनों मिलकर <strong>Cardiac Risk</strong> को काफी बढ़ा देते हैं।</p>

<h3>7. धूम्रपान (Smoking)</h3>
<p><strong>Smoking</strong> धमनियों की परत (Endothelium) को तुरंत नुकसान पहुंचाता है, रक्त के थक्के बनने की प्रक्रिया को तेज करता है और <strong>Heart Attack</strong> का सबसे बड़ा रोकथाम योग्य (Preventable) कारण है।</p>

<h3>8. तनाव (Stress)</h3>
<p>लगातार तनाव (Chronic Stress) <strong>Blood Pressure</strong> बढ़ाता है और शरीर में <strong>Cortisol</strong> का स्तर बढ़ाता है, जो हृदय के लिए हानिकारक है।</p>

<h2>जेनेटिक हृदय रोग का पता कैसे लगाएं? (Genetic Testing)</h2>
<p>यदि आपके परिवार में <strong>Early Heart Disease</strong> का इतिहास है, तो आप इन परीक्षणों (Tests) से अपने जोखिम का आकलन कर सकते हैं:</p>
<ul>
<li><strong>लिपिड प्रोफाइल (Lipid Profile):</strong> Total Cholesterol, LDL, HDL और <strong>Triglycerides</strong> की जांच।</li>
<li><strong>कार्डियक बायोमार्कर्स:</strong> <strong>Hs-CRP</strong> (हृदय संबंधी सूजन के लिए) और <strong>BNP/NT-proBNP</strong> (हृदय विफलता के लिए)।</li>
<li><strong>इलेक्ट्रोकार्डियोग्राम (ECG/EKG):</strong> हृदय की विद्युत गतिविधि (Electrical Activity) की जांच।</li>
<li><strong>इकोकार्डियोग्राम (2D Echocardiography):</strong> हृदय की संरचना और कार्य का अल्ट्रासाउंड।</li>
<li><strong>स्ट्रेस टेस्ट (TMT/Stress Test):</strong> व्यायाम के दौरान हृदय की क्षमता की जांच।</li>
<li><strong>जेनेटिक परीक्षण (Genetic Testing):</strong> FH (Familial Hypercholesterolemia) जैसे विशिष्ट <strong>Genetic Heart Conditions</strong> के लिए।</li>
</ul>

<h2>अपने Heart को कैसे बचाएं? (Prevention Tips)</h2>
<p>भले ही आपका <strong>Family History</strong> आपको <strong>Heart Attack</strong> का खतरा बढ़ाता है, लेकिन जीवनशैली में कुछ बदलाव (Lifestyle Changes) से आप इस जोखिम को काफी हद तक कम कर सकते हैं:</p>

<h3>1. स्वस्थ आहार अपनाएं (Heart-Healthy Diet)</h3>
<p>फल, सब्जियां, साबुत अनाज (Whole Grains), मछली (Omega-3 fatty acids) और मेवे अपने आहार में शामिल करें। <strong>Trans fats</strong>, ज्यादा नमक और <strong>Processed foods</strong> से बचें। <strong>Mediterranean Diet</strong> हृदय के लिए सबसे अच्छा माना जाता है।</p>

<h3>2. नियमित व्यायाम करें (Regular Exercise)</h3>
<p>सप्ताह में कम से कम 150 मिनट मध्यम व्यायाम (जैसे तेज चाल/Brisk walking) या 75 मिनट जोरदार व्यायाम करें। <strong>Regular Exercise</strong> <strong>Blood Pressure</strong>, कोलेस्ट्रॉल और वजन को नियंत्रित रखता है।</p>

<h3>3. धूम्रपान त्यागें (Quit Smoking)</h3>
<p><strong>Smoking</strong> छोड़ने के 1 वर्ष के भीतर <strong>Heart Disease</strong> का खतरा आधा हो जाता है। निकोटीन रिप्लेसमेंट थेरेपी (NRT) या डॉक्टर की सलाह लें।</p>

<h3>4. वजन प्रबंधन (Weight Management)</h3>
<p><strong>BMI</strong> (Body Mass Index) 18.5-24.9 के बीच रखें। <strong>Waist Circumference</strong> पुरुषों में 40 इंच और महिलाओं में 35 इंच से कम होना चाहिए।</p>

<h3>5. नियमित स्वास्थ्य जांच (Regular Health Check-ups)</h3>
<p>40 साल से ऊपर के सभी व्यक्तियों को साल में कम से कम एक बार <strong>Full Body Check-up</strong> जरूर कराना चाहिए। <strong>Sankalp Hospital</strong> में हम विशेष <strong>Cardiac Health Packages</strong> प्रदान करते हैं।</p>

<h3>6. तनाव प्रबंधन (Stress Management)</h3>
<p>योग (Yoga), ध्यान (Meditation), गहरी सांस लेने के व्यायाम (Deep Breathing) और शौक (Hobbies) तनाव को कम करने में मदद करते हैं।</p>

