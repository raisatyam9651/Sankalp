    <p class="lead">Knee swelling, often called "water on the knee" (effusion), can be painful and limit your mobility. Whether it's caused by a sudden injury or a chronic condition like arthritis, knowing how to manage it quickly is essential for recovery.</p>

    <h2>The R.I.C.E. Method: Your First Line of Defense</h2>
    <p>The R.I.C.E. method is the gold standard for reducing acute swelling and inflammation:</p>
    <ul>
      <li><strong>Rest:</strong> Avoid putting weight on the affected knee. Give your body time to heal.</li>
      <li><strong>Ice:</strong> Apply an ice pack for 15-20 minutes every 2-3 hours for the first 48 hours. This constricts blood vessels and reduces fluid buildup.</li>
      <li><strong>Compression:</strong> Use an elastic bandage to provide gentle support and prevent further swelling. Ensure it's not too tight to cut off circulation.</li>
      <li><strong>Elevation:</strong> Keep your knee elevated above the level of your heart using pillows. This helps drain excess fluid away from the joint.</li>
    </ul>

