    <h3>1. पैरों को ठंडे पानी में भिगोएं</h3>
    <p>गर्म पानी से जलन और भी बढ़ सकती है, इसलिए ठंडे पानी का उपयोग करें। 15-20 मिनट तक पैरों को ठंडे पानी में रखें। यह तुरंत राहत देता है। लेकिन बर्फीला पानी न लें - इससे त्वचा को नुकसान हो सकता है।</p>

    <h3>2. पैरों को उंचा रखें</h3>
    <p>बैठे या लेटे समय पैरों को तकिये की मदद से उंचा रखें। यह खून के बहाव को बेहतर बनाता है और जलन कम करता है। दिन में कई बार 15-20 मिनट तक ऐसे रहें।</p>

    <h3>3. आरामदायक जूते पहनें</h3>
    <p>तंग या कसकर जूते न पहनें। सांस लेने वाले जूते चुनें जिनमें पैरों को आराम मिले। नए जूते पहनते समय मोज़े पहनें।</p>

    <h3>4. व्यायाम करें</h3>
    <p>हल्के व्यायाम से खून का बहाव बेहतर होता है। पैरों की उंगलियों को हिलाने के अभ्यास करें। पैरों को गोल-गोल घुमाएं। रोज़ाना 30 मिनट तक चलना फायदेमंद है।</p>

    <h3>5. मैग्नीशियम और पोटेशियम</h3>
