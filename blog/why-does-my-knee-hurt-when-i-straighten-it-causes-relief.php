    <p class="lead">Feeling pain when you try to fully extend your leg is a common orthopedic complaint. This type of pain can be a sign of various issues within the joint, ranging from minor inflammation to structural damage.</p>

    <h2>Common Causes of Pain During Extension</h2>
    <p>Understanding where and when the pain occurs can help identify the cause:</p>
    <ul>
      <li><strong>Meniscus Tear:</strong> A piece of torn cartilage can get caught in the joint, physically blocking full extension and causing sharp pain.</li>
      <li><strong>Knee Bursitis:</strong> Inflammation of the fluid-filled sacs around the joint can cause pain and pressure when the leg is straightened.</li>
      <li><strong>Osteoarthritis:</strong> Wear and tear of the joint cartilage can lead to stiffness and pain during any movement, including straightening.</li>
      <li><strong>Quadriceps Tendonitis:</strong> Inflammation of the tendon just above the kneecap can make straightening the leg painful.</li>
      <li><strong>Baker's Cyst:</strong> A fluid-filled cyst behind the knee that can cause a feeling of tightness and pain when extending the leg.</li>
    </ul>

    <div class="relief-box">
      <h4>Finding Relief: Initial Steps</h4>
      <ul>
        <li><strong>Avoid Forcing Extension:</strong> If your knee is locked or very painful, don't try to force it straight.</li>
        <li><strong>Gentle Stretching:</strong> Once cleared by a doctor, specific stretches can help improve flexibility.</li>
        <li><strong>Pain Management:</strong> Over-the-counter anti-inflammatories can help reduce swelling and pain.</li>
        <li><strong>Professional Physical Therapy:</strong> A therapist can provide targeted exercises to address the root cause of the limitation.</li>
      </ul>
    </div>

    <h2>When to Consult a Specialist</h2>
    <p>If your knee feels "locked" and you physically cannot straighten it, or if the pain is accompanied by significant swelling, you should see an orthopedic specialist immediately.</p>
